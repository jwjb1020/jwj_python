#초를 입력하면 분과 초로 표시하는 프로그램. 예를 들어, 200초를 입력하면 3분 20초로 표현하라
isecond = int(input("초를 입력해주세요."))
min=isecond/60
psecond=isecond%60
print(f"{min:.0f}분{psecond}초")
#분(min)을 입력 하면, 일, 시간, 분으로 출력하는 프로그램을 만들어라. (예 : 1550분은 1일 1시간 50분)
min2=int(input("분을 입력하세요"))
day=min2//(24*60)
hour=(min2//60)%24
min3=min2%60
print(f"{day}일{hour}시간{min3}분")

